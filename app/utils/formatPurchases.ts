export function formatPurchases(wallets: string[], purchases: number[]) {
  let formattedList: { index: number; wallet: string; purchases: number[] }[] =
    [];

  if (wallets.length === purchases.length) {
    let currentIndex = 0;

    for (let index = 0; index < wallets.length; index++) {
      const itemIndex = formattedList.findIndex(
        (item) => item.wallet === wallets[index]
      );

      if (itemIndex >= 0) {
        formattedList[itemIndex].purchases.push(purchases[index]);
      } else {
        formattedList.push({
          index: currentIndex,
          wallet: wallets[index],
          purchases: [purchases[index]],
        });
        currentIndex++;
      }
    }
  }

  formattedList.map((item) =>
    console.log(
      `wallets[${item.index}]=address(${item.wallet}); tokensPurchased[${
        item.index
      }]=${item.purchases.reduce((acc: number, curr: number) => acc + curr, 0)}`
    )
  );

  console.log("END");
  console.log(" ");

  return formattedList;
}
